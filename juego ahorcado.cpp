void escoger_palabra()
{
	archivo.open("ahorcado.txt",ios::in);
	
	if (archivo.fail())
	{
		cout<<"el archivo no ha sido encontrado";
		exit(1);
	}
	
	srand(time(NULL));
	do
	{
	palabraescogida=rand()%16; // variar el 11 por el numero de palabras que esten en el archivo ya que si el archivo tiene por ejemplo 3 palabras, el programa buscara 11 palabras, teniendo 8 campos vacios y entrara en un bucle
	}while(palabraescogida==0);
	
	for (buscarpalabra=0;buscarpalabra<palabraescogida;buscarpalabra++)
	{
		
		for(mov=0;mov<20;mov++)
		{
			palabra[mov]=0;
			palabra_victoria[mov]=0;
		}
		
		if(!archivo.eof())
		{
		archivo.getline(palabra,20);
		
		}
	}
	
	archivo.close();
	espacios();
}


